print("-----------------------------------------------------------------------------------")
print("1. String Operation : ")
print("-----------------------------------------------------------------------------------")
print("Length of string : ", len("Hello World"))
print("Upper case : ", "hello".upper())
print("Lower case : ", "HELLO".lower())
print("Minimum value : ", min([5, 2, 8, 1, 9]))
print("Maximum value : ", max([5, 2, 8, 1, 9]))
print("Sorted list : ", sorted([5, 2, 8, 1, 9]))
print("Title :", "hello world".title())
print("Capitalized :", "hello world".capitalize())
print("Split :", "Hello World".split())
print("Replace :", "Hello World".replace("World", "Python"))
print("Join :", "-".join(["Hello", "World"]))
print("Find :", "Hello World".find("World"))
print("Starts With :", "Hello World".startswith("Hello"))
print("endswith :", "Hello World".endswith("World"))
print("Swapcase :", "Hello World".swapcase())
print("Strip :", "   Hello World   ".strip())
print("Rstrip :", "   Hello World   ".rstrip())
print("IsAlpha :", "Hello".isalpha())
print("IsDigit :", "123".isdigit())
