print("2. List Operation : ")
print("-----------------------------------------------------------------------------------")
num_list = [1, 2, 3, 4, 5]
num_list.append(6)
print("Append :", num_list) 
num_list.extend([7, 8, 9])
print("Extend :", num_list)
num_list.insert(0, 0)
print("Insert :", num_list)
num_list.remove(3)
print("Remove :", num_list)
num_list.pop()
print("Pop :", num_list)
num_list.sort()
print("Sort :", num_list)
num_list.reverse()
print("Reverse :", num_list)
print("Index of 4 :", num_list.index(4))
print("Count of 2 :", num_list.count(2))
print("Length of list :", len(num_list))
print("Clear list : ", num_list.clear())
